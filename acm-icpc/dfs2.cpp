#include "dfs2.h"
#include <iostream>
char field[10][12] = {
        {'W','.','.','.','.','.','.','.','.','W','W','.'},
        {'.','W','W','W','.','.','.','.','.','W','W','W'},
        {'.','.','.','.','W','W','.','.','.','W','W','.'},
        {'.','.','.','.','.','.','.','.','.','W','W','.'},
        {'.','.','.','.','.','.','.','.','.','W','.','.'},
        {'.','.','W','.','.','.','.','.','.','W','.','.'},
        {'.','W','.','W','.','.','.','.','.','W','W','.'},
        {'W','.','W','.','W','.','.','.','.','.','W','.'},
        {'.','W','.','W','.','.','.','.','.','.','W','.'},
        {'.','.','W','.','.','.','.','.','.','.','W','.'},
};
void dfs2(int x, int y){
    field[x][y] = '.';
    for (int dx = -1; dx <= 1; ++dx) {
        for (int dy = -1; dy <= 1 ; ++dy) {
            int nx = x + dx;
            int ny = y + dy;
            if(
                0<=nx && nx<10 &&
                0<=ny && ny<12 &&
                field[nx][ny] == 'W'){
                dfs2(nx,ny);
            }
        }

    }

}
int solve(){
    int res = 0;
    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < 12; ++j) {
            if(field[i][j] == 'W'){
                dfs2(i,j);
                res++;
            }
        }
    }
    return res;
}